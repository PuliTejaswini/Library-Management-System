    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(txtId.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please enter valid Id and search it");
            txtId.requestFocus();
        }
        else{
            try{
            pst=con.prepareStatement("SELECT * FROM library.Book where Id=?");
            pst.setString(1, txtId.getText());
            r=pst.executeQuery();
            if(r.next()){
                txtname.setText(r.getString(2));
            }
            } catch (SQLException ex) {
                Logger.getLogger(IssueDate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(txtId.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Please enter valid Id and search it");
            txtId.requestFocus();
        }
        else{
            try{
            pst=con.prepareStatement("UPDATE `library`.`book` SET `status` = 'ISSUED', issuedate = ?, duedate = ?,studentid =? WHERE (`Id` = ?)");
            pst.setString(1, txtissuedate.getText());
            pst.setString(2, txtduedate.getText());
            pst.setString(3, txtstudentid.getText());
            pst.setString(4, txtId.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Book Issued");
            } catch (SQLException ex) {
                Logger.getLogger(IssueDate.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        
    }//GEN-LAST:event_jButton1ActionPerformed

